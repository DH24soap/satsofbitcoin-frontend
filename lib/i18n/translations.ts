export function translate(
  language: string,
  key: TranslationKey,
  vars?: Record<string, string | number>
): string {
  const dict = TRANSLATIONS[language] ?? en;
  let text = dict[key] ?? en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replaceAll(`{${k}}`, String(v));
    }
  }
  return text;
}
